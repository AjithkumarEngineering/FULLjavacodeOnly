package ajavacode;

public class ConditionalStatement {
	public static void main(String[] args) {
		int a =5;
		if (a>0) {
			System.out.println("The "+a+" is Positive Number");
		}
		else if(a<0){
			System.out.println("The "+a+" is Negative Number");
		}
		else {
			System.out.println("The Value is = " +a);
		}
	}

}
